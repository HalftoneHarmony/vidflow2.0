④ 쿠키 설정 거부 방법
- Internet Explorer: 도구 > 인터넷 옵션 > 개인정보 > 설정
- Chrome: 설정 > 고급 > 개인정보 및 보안 > 콘텐츠 설정 > 쿠키
- Safari: 환경설정 > 개인정보

